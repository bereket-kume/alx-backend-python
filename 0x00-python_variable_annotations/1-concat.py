#!/usr/bin/env python3
"""
    function concat that takes a string str1 and 
    a string str2 as arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
        Args:
            str1: The first string parameter
            str1: The second string parameter

        Return:
            Concatenated string
    """
    return str1 + str2
